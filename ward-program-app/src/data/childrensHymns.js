// Get song title by number (e.g. '2', '20a', '20b')
export const getChildrensHymnTitle = (number) => {
  const key = String(number).trim();
  return CHILDRENS_HYMNS[key]?.title ?? null;
};

// Get song slug by number
export const getChildrensHymnSlug = (number) => {
  const key = String(number).trim();
  return CHILDRENS_HYMNS[key]?.slug ?? null;
};

// Get the full church website URL for a children's song
export const getChildrensHymnUrl = (number) => {
  const key = String(number).trim();
  const song = CHILDRENS_HYMNS[key];
  if (!song) return null;
  return `https://www.churchofjesuschrist.org/study/manual/childrens-songbook/${song.slug}?lang=eng`;
};