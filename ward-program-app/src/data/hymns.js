// Get hymn title by number
export const getHymnTitle = (number) => {
  const num = parseInt(number);
  return HYMNS[num]?.title || null;
};

// Get hymn slug by number (null for classic hymns)
export const getHymnSlug = (number) => {
  const num = parseInt(number);
  return HYMNS[num]?.slug || null;
};

// Get the web URL for a hymn
// Classic hymns (1–341): legacy URL format
// New hymns (1000+): slug-based URL from churchofjesuschrist.org
export const getHymnUrl = (number) => {
  const num = parseInt(number);
  if (!num || !HYMNS[num]) return null;

  const { slug } = HYMNS[num];

  if (slug) {
    // New hymns — use slug-based URL
    return `https://www.churchofjesuschrist.org/study/music/hymns-for-home-and-church/${slug}?lang=eng`;
  } else {
    // Classic hymns — use legacy number-based URL
    return `https://www.churchofjesuschrist.org/music/library/hymns/${num}?lang=eng`;
  }
};

